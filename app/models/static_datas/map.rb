class StaticDatas::Map

	MAX_Q=35
	MAX_R=60

	extend Assert

	def self.terrain(q,r)
		h = $map[[q,r]]
		h ? h : :out
	end

	def self.get_color_map
		$map.keys.map{ |k| [ k[0], k[1], terrain_color( k[0], k[1] ) ] }
	end

	private

	def self.terrain_color(q,r)
		t = terrain( q, r )
		assert( t && t != :out, "No terrain for (q, r) = (#{q.inspect}, #{r.inspect}) t = #{t.inspect}", self.class, __method__ )
		assert( COLORS.has_key?( t ), "No colors for #{t.inspect}", self.class, __method__ )
		COLORS[ t ]
	end

